/// Доминирующее состояние экрана по данным (SSS UI SYSTEM v2 §2.1).
///
/// Не обязан покрывать весь апп одним enum на виджет — но **один явный источник фазы**
/// на экран упрощает ветвление, тесты и онбординг дизайна.
enum UiScreenPhase {
  /// Данные ещё не готовы (первый запрос, нет кэша).
  loading,

  /// Есть консистентный снимок для UI.
  ready,

  /// Показан кэш / предыдущее значение, идёт фоновое обновление.
  stale,

  /// Явный refresh (pull-to-refresh, sync) — при необходимости отдельно от [stale].
  updating,

  /// Нет доверенных данных.
  error,

  /// Валидное «пусто» (0 операций и т.п.).
  empty,

  /// Часть данных недоступна, остальное OK (например hero ждёт снимок, лента уже есть).
  partial,
}
